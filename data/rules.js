// 自动扩展 M×T 行为模式：补齐 6 种 A/C/R 排列
// ====================================================

// 所有可能的 A/C/R 排列组合
const MT_PERMUTATIONS = [
  ['A', 'C', 'R'],
  ['A', 'R', 'C'],
  ['C', 'A', 'R'],
  ['C', 'R', 'A'],
  ['R', 'A', 'C'],
  ['R', 'C', 'A']
];

function expandMTPatterns(raw) {
  const expanded = { ...raw };

  Object.entries(raw).forEach(([key, value]) => {
    const [mPart, tPart] = key.split('__');   // 例如 "R>C>A", "Neu_high"
    if (!mPart || !tPart) return;

    const codes = mPart.split('>');
    if (codes.length !== 3) return;           // 不是 3 个字母就跳过

    MT_PERMUTATIONS.forEach((perm) => {
      const permKey = `${perm.join('>')}__${tPart}`;

      // 只在「原来没有写」的情况下，才自动补一条
      if (!expanded[permKey]) {
        expanded[permKey] = value;
      }
    });
  });

  return expanded;
}

// 对外暴露的 M_T_patterns：已经自动补齐所有缺的组合
export const M_T_patterns = expandMTPatterns(RAW_M_T_PATTERNS);

